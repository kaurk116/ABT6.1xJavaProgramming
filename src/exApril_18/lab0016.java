package exApril_18;

public class lab0016 {
    public static void main(String[] args){
        //increment operators
        //pre increment
        int a =10;
        int result= ++a;
        System.out.println(result);
        System.out.println(a);

        //post increment
        int b =10;
        int result2 = b++;
        System.out.println(result2);
        System.out.println(b);
    }
}
